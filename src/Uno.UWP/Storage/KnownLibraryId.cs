namespace Windows.Storage;

/// <summary>
/// Provides values that identify storage libraries.
/// </summary>
public enum KnownLibraryId
{
	/// <summary>
	/// Music library.
	/// </summary>
	Music = 0,

	/// <summary>
	/// Pictures library.
	/// </summary>
	Pictures = 1,

	/// <summary>
	/// Videos library.
	/// </summary>
	Videos = 2,

	/// <summary>
	/// Documents library.
	/// </summary>
	Documents = 3,
}
