namespace Windows.Storage;

// TODO:MZ: Mark unsupported ones as NotImplemented

/// <summary>
/// Provides values that identify storage folders.
/// </summary>
public enum KnownFolderId
{
	/// <summary>
	/// App Captures folder.
	/// </summary>
	AppCaptures = 0,

	/// <summary>
	/// Camera Roll folder.
	/// </summary>
	CameraRoll = 1,

	/// <summary>
	/// Documents library folder.
	/// </summary>
	DocumentsLibrary = 2,

	/// <summary>
	/// HomeGroup folder.
	/// </summary>
	HomeGroup = 3,

	/// <summary>
	/// The folder of media server (Digital Living Network Alliance (DLNA)) devices.
	/// </summary>
	MediaServerDevices = 4,

	/// <summary>
	/// Music library folder.
	/// </summary>
	MusicLibrary = 5,

	/// <summary>
	/// Objects 3D folder.
	/// </summary>
	Objects3D = 6,

	/// <summary>
	/// Pictures library folder.
	/// </summary>
	PicturesLibrary = 7,

	/// <summary>
	/// Playlists folder.
	/// </summary>
	Playlists = 8,

	/// <summary>
	/// Recorded calls folder.
	/// </summary>
	RecordedCalls = 9,

	/// <summary>
	/// Removable devices folder.
	/// </summary>
	RemovableDevices = 10,

	/// <summary>
	/// Saved pictures folder.
	/// </summary>
	SavedPictures = 11,

	/// <summary>
	/// Screenshots folder.
	/// </summary>
	Screenshots = 12,

	/// <summary>
	/// Videos library folder.
	/// </summary>
	VideosLibrary = 13,

	/// <summary>
	/// All app modifications folder.
	/// </summary>
	AllAppMods = 14,

	/// <summary>
	/// Current app modifications folder.
	/// </summary>
	CurrentAppMods = 15,

	/// <summary>
	/// Downloads library folder.
	/// </summary>
	DownloadsFolder = 16,
}
